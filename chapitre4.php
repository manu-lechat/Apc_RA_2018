            <span class="content">L’exemple de l’éco-gestionnaire&#160;:</span><br>
            <span class="content">du bâtiment à l’îlot</span><br>
          </h3>

          <p>La transition énergétique crée des emplois, transforme des fonctions et permet l’émergence de nouveaux métiers. </p>
          <p>Dans le cadre des réflexions portées par la mission Ville Intelligente et Durable de la Ville de Paris (MIVIDA), un groupe de travail a rassemblé 7 acteurs de l‘immobilier et de la ville durable autour de la question de l’évolution à venir du
métier du property manager et de ses nouvelles missions, dans une perspective de développement durable urbain.</p>
