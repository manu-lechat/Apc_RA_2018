          <h3 class="titre_bgcolor">
            <span class="content">Lancement du Plan national</span><br>
            <span class="content">de la rénovation du bâtiment</span><br>
          </h3>
          <p class="txt_intro">L’Agence Parisienne du Climat a accueilli le 24 novembre 2017 Nicolas Hulot, Ministre d’Etat, Ministre de la Transition Ecologique et Solidaire et Jacques Mézard, Ministre de la Cohésion des Territoires pour lancer la concertation sur le plan national de la rénovation énergétique des bâtiments. L’enjeu&#160;: contribuer à atteindre la neutralité carbone en 2050, en privilégiant la rénovation des bâtiments les plus énergivores. Nicolas Hulot rappelle que la rénovation énergétique des bâtiments est sa priorité en matière d’énergie.
          </p>
          <p>La visite des Ministres s’est déroulée en deux temps&#160;: la visite du chantier de rénovation énergétique de la copropriété Beccaria à Paris 12<sup>e</sup> et une table ronde qui a réuni les acteurs nationaux majeurs.</p>
            <div class="img_container"><img src="img/chapitre4/plan_climat.jpg" alt=""></div>

          <h4 class="titre_majBoldColor">Les propositions de l’Agence<br>Parisienne du Climat</h4>
          <p>Agence d’accélération de la transition énergétique à Paris et tiers de confiance auprès des copropriétaires et des professionnels, l’Agence Parisienne du Climat préconise d’intégrer en tant que tel l’enjeu stratégique de la rénovation des copropriétés dans le plan national de la rénovation énergétique des bâtiments.</p>
          <p>À l’enjeu énergétique et climatique certain, au moins deux autres arguments plaident en faveur d’un traitement spécifique des copropriétés&#160;:
          </p>
          <ul>
          <li><span class="bold">Répondre à un enjeu social</span>&#160;: 37% des ménages en situation de précarité énergétique sont logés en copropriété,</li>
          <li><span class="bold">Valoriser la copropriété</span> comme un outil essentiel de lutte contre l’étalement urbain&#160;: il s’agit du choix d’habitat durable par excellence.</li>
          </ul>

          <h4 class="titre_majBold">L’APC fait 9 propositions dont la première&#160;: reconnaître les spécificités de la copropriété.</h4>
          <p>Il s’agit d’un segment spécifique du secteur résidentiel.  Pour favoriser la massification des rénovations énergétiques sur ce segment du marché, celui-ci doit être traité en intégrant ses spécificités&#160;:</p>
          <ul class="small_list">
          <li><span class="bold">Temporelle</span>&#160;: les projets s’y inscrivent dans le moyen terme,</li>
          <li><span class="bold">Sociale</span>&#160;: les projets y sont par nature collectifs, partagés, solidaires,</li>
          <li><span class="bold">Organisationnelle</span>&#160;: les projets s’y mènent avec un écosystème professionnel spécifique et avec une gouvernance particulière, condition essentielle du succès du projet.</li>
          </ul>

          <p class="encart_bgColor"><span class="bold">Il ne faut pas avoir peur de la copropriété</span>&#160;: prise dans ses spécificités, elle se rénove. Le travail mené par l’APC avec le déploiement de la plateforme CoachCopro sur <span class="bold">21 territoires</span> dans les principales agglomérations françaises en donne la preuve.</p>



