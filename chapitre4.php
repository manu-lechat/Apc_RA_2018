          <p class="txt_intro">Fin 2016, la Ville de Paris a lancé un appel à expérimentation dédié à l’adaptation au changement climatique, conduit par Paris&Co en partenariat avec l’Agence Parisienne du Climat, Eau de Paris et <a href="http://www.100resilientcities.org/" target="_blank">100 Resilient Cities</a>. Cet appel à expérimentation a pour but de faire émerger des projets innovants, capables d’apporter des réponses concrètes sur 4 grandes thématiques&#160;: le rafraîchissement, l’économie des ressources en eau, le renforcement du lien social et la modularité des espaces et des modes de vie.</p>

          <p>En Mars 2017, le jury, composé de <span class="bold">Célia Blauel</span> et <span class="bold">Jean-Louis Missika</span>, adjoints à la Maire de Paris, et des représentants des partenaires de l’appel, retient <span class="bold">10 projets innovants</span> apportant des <span class="bold">réponses concrètes</span> aux enjeux de l’adaptation.</p>
          </div>

          <div class="col_33pc">
          <p class="txt_verbatim">«&#8239;Les projets retenus participent à la construction d’une ville qui anticipe et s’adapte aux effets du dérèglement climatique qui touchent la capitale. Notre objectif&#160;: mobiliser toutes les formes d’innovation pour faire la ville du 21<sup>e</sup> siècle&#8239;»</p>
          <p class="signature_verbatim"><span class="bold">Célia Blauel</span>, adjointe à la Maire de Paris en charge de l’environnement, du développement durable, de l’eau, de la politique des canaux et du «&#8239;Plan Climat énergie territorial&#8239;».</p>

          <p class="encart_bgColor"><span class="bold">Soutien des lauréats dans le développement de leur projet ou recherche de terrain d’expérimentation</span></br>
          Dans le cadre de l’appel à expérimentation dédié à l’adaptation au changement climatique, l’Agence Parisienne du Climat a fait bénéficier aux porteurs de projet de son expertise et de son réseau notamment pour trouver des terrains d’expérimentations. </p>


