        <div class="col_33pc">
          <h3 class="titre_bgcolor">
            <span class="content">Une planification intégrée de l’énergie</span><br>
            <span class="content">dans l’aménagement urbain</span><br>
          </h3>

          <p>Le succès des politiques urbaines de lutte contre le changement climatique dépend de la capacité des villes à promouvoir des aménagements sobres en carbone et résilients. Les choix effectués aujourd’hui en matière d’aménagement auront des effets pour les clinquantes prochaines années et au-delà. C’est donc aujourd’hui qu’il faut prévoir les villes de demain.</p>
          <p>Dans cette optique, l’Agence a développé une expertise sur l’intégration de l’énergie dans le processus d’aménagement urbain par sa contribution aux travaux du programme européen Urban Learning qui a livré ses résultats fin 2017.</p>
          <p>L’objectif du projet Urban Learning «&#8239;Integrative energy planning of urban areas&#160;: collective learning for improved governance», est de généraliser et institutionnaliser une planification intégrée de l'énergie dans l’aménagement urbain. L'accent est mis sur les processus de gouvernance liés au (re)développement de sites urbains (zones d’aménagement).</p>
          <img src="img/chapitre4/urban_learning.png" alt="">
          <p>Le projet “Urban learning” associe les villes de Paris, Vienne, Berlin, Stockholm, Amsterdam/Zaanstad, Varsovie et Zagreb, avec une problématique commune&#160;: faire face à la croissance démographique urbaine tout en en réduisant la consommation d'énergie et les émissions de gaz à effet de serre. </p>

                     <p class="titre_majBoldColor">Les villes engagées doivent&#160;:</p>
          <ul>
          <li><span class="bold">consolider et partager leurs expériences</span> et connaissances actuelles,</li>
          <li><span class="bold">analyser les processus de planification urbaine</span> (gouvernance et outils) et identifier les éléments de freins et leviers,</li>
          <li><span class="bold">identifier et mesurer les manques</span> (moyens et capacités) au sein de ces processus, afin de trouver des solutions tout en associant les autres acteurs du territoire impliqués,</li>
          <li><span class="bold">proposer des changements réels</span> dans les processus de gouvernance.</li>
         </ul>

