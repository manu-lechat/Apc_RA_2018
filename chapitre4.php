          <h1 class="appear_on_scroll">
            <span class="content">4. Animer le laboratoire</span><br>
            <span class="content">de la transition énergétique</span>
          </h1>
      </div>


      <div class="row">
        <p class="txt_bigIntro">

          La transition énergétique est créatrice de solutions d’avenirs et d’évolutions sociétales positives pour les habitants et les territoires. Pour contribuer à l’émergence de dispositifs opérationnels et à l’évolution des pratiques et des métiers, l’Agence déploie trois logiques d’actions&#160;:
          <br>contribuer aux politiques publiques énergie-climat&#8239;;
          <br>rester en veille sur les nouveaux champs d’actions structurants de la transition énergétique, et identifier les nouveaux métiers, les nouvelles fonctions&#8239;;
          <br>innover et expérimenter.

      </p>
      </div>

            <i id="chapitre41"></i>

          <div class="row">
            <h2 class="titre_h2">
              <span>Décrypter et contribuer</span><br>
              <span>aux politiques publiques </span> </h2>
          </div>


      <div class="row">
        <div class="col_33pc">

          <p class="txt_intro">Les politiques publiques de réduction des émissions de gaz à effet de serre s’actualisent et évoluent. Dans ce contexte mouvant, nombreuses sont les questions que peuvent se poser les décideurs ; l’objectif du travail de décryptage, sans être ni critique, ni conseil, est d’obtenir une visibilité d’ensemble. L’objectif pour l’Agence est également de contribuer régulièrement aux consultations nationales, régionales et locales sur la transition énergétique. Enfin, l’adaptation du territoire au changement climatique est un volet opérationnel majeur sur lequel l’Agence s’est investie en 2017.
          </p>


          <p class="txt_verbatim">Cette mission a été essentielle en cette année d’élaboration du nouveau Plan Climat de Paris et du Plan Climat Air Energie Métropolitain.
          </p>

        </div>
        <div class="col_33pc">

          <h3 class="titre_bgcolor">
            <span class="content">Contribution à l’élaboration du nouveau</span><br>
            <span class="content">Plan Climat Air Energie de Paris</span><br>
          </h3>

          <p>Une part importante du travail de 2017 a été consacrée au Nouveau Plan Climat de Paris. L’APC a en effet été mobilisée pour co-organiser des ateliers de concertation avec les acteurs économiques et institutionnels du Plan Climat (voir partie «&#8239;Mobilisation des acteurs économiques&#8239;»).
          </p>
          <p>L’APC a aussi livré à la Ville de Paris sa propre contribution au Nouveau Plan Climat sous la forme  d’un «&#8239;Cahier d’Acteurs&#8239;», formulant des propositions pour l’avenir de l’Agence à court et moyen terme en vue d’atteindre l’objectif de neutralité carbone en 2050.
          </p>
          <p>L’APC a ainsi convié l’ensemble de ses adhérents et partenaires à une matinée de travail collaboratif le 17 mars 2017.  La synthèse de cet atelier des cahiers d’acteurs de l’APC a été publiée dans le <a href="https://api-site-cdn.paris.fr/images/91103" target="_blank">bilan de cette concertation</a> de la Ville de Paris).</p>
        </div>

