            <span class="content">Décryptage des politiques</span><br>
            <span class="content">de la transition énergétique</span><br>
            <span class="content">métropolitaine</span><br>
          </h3>
          <p class="txt_intro">Conformément à sa mission de décryptage des politiques climat-énergie en vigueur sur le territoire parisien, l’APC organise une conférence dédiée «&#8239;Les Actus du Climat&#8239;» à destination des adhérents et des professionnels de son réseau.  Le 22 Juin 2017, elle a réuni des intervenants de la Mairie de Paris, de la Métropole du Grand Paris, de l’APUR, de l’IAU et de l’ARENE qui ont partagé leurs projets en cours (Outils présentés&#160;: ROSE, ENERGIF, GESi, cartographie «&#8239;chaleur en ville&#8239;», …).
          </p>
          <p>La Métropole du Grand Paris a notamment présenté sa démarche et les réflexions à mener dans une perspective élargie dans le cadre de l’élaboration de son Plan Climat. L’accent est également mis sur&#160;:</p>
        <ul>
            <li>Les points saillants du volet adaptation&#160;: la vulnérabilité du territoire aux îlots de chaleur urbain, l’enjeu de la désimperméabilisassions des sols, de nouveaux modes de vie avec l’agriculture urbaine et les circuits courts d’approvisionnement,</li>
