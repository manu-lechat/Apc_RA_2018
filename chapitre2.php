          Le conseil est le cœur de métier de l’Agence. Les conseillers, experts du quotidien, sont reconnus comme tiers de confiance tant par les particuliers que les professionnels qui souhaitent réduire leur consommation d’énergie ou se tourner vers les énergies renouvelables.
          </p>
           <p class="txt_verbatim">
           «&#8239;Au moindre doute notre conseiller fait les recherches pour répondre à notre cas particulier, aucune question n'est évincée, nos dossiers ont été supervisés avec sérieux, et nous sommes recontactés régulièrement pour le suivi de l'avancement du projet&#8239;»
