    <p>La charte <span class="bold"> «&#8239;Paris Action Climat&#8239;» </span>évolue en 2018 pour s’adapter au nouveau Plan Climat et proposer une démarche plus ouverte aux entreprises et institutions.  Elle croise à la fois les grands Objectifs de Développement Durable de l’ONU et les mesures du plan avec une approche territoriale. Les acteurs signataires s’engagent sur l’un des 3 niveaux d’implication proposés allant de l’adhésion aux principes de la charte jusqu’à la mise en œuvre concrète d’un plan d’actions à 2030.  La nouvelle charte sera co-pilotée avec l’Agence Parisienne du Climat et intégrera les communautés d’acteurs. </p>
