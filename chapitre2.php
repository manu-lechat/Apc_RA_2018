          <h2 class="titre_bgcolor">
            <span>Mieux maîtriser</span><br>
            <span>sa consommation :</span><br>
            <span>Le défi FAEP</span>
          </h2>
            <p class="txt_intro">
            Dispositif pédagogique et ludique, le défi Familles à Energie Positive rassemble chaque année des particuliers organisés en équipe qui souhaitent réduire leur consommation d’énergie sur la période de chauffe.
          </p>
        <p>Fort du soutien des mairies d’arrondissement, la 6<sup>e</sup> édition du défi Familles à Energie Positive (FAEP) a réuni 200 foyers, organisés en 35 équipes, qui ont cherché pendant plusieurs mois à économiser au moins 8% sur leurs factures d’énergie et d’eau par rapport à l’hiver précédent. Les résultats peuvent aller bien au-delà de cet objectif : en 2017, l’une des équipes est parvenue à faire 25% d’économies.
  </p>
<p><span class="bold">Ouvert aux entreprises depuis 2 ans,</span> la Fondation de France et La Poste/service Courrier Paris ont proposé FAEP à leurs salariés.</p>

<p class="txt_verbatim">
  «&#8239;Dans le cadre de notre politique RSE, nous avons proposé à nos postiers parisiens de participer au Défi, avec pour objectif de les sensibiliser aux enjeux du changement climatique à travers des actions concrètes et faciles à mettre en place. C’est la deuxième édition et le bilan est très positif : les salariés sont satisfaits des économies réalisées (-15% sur l’énergie consommée, soit 1,2 tonne d’émissions CO2 évitée, l’équivalent de 6 trajets en voiture Paris–Marseille !), du gain financier engendré et du lien social qui s’est tissé entre les participants d’un même site qui ne se connaissaient pas toujours. C’est un Défi que nous souhaitons reconduire l’année prochaine&#8239;»
</p>
<p class="signature_verbatim"><span class="bold">Carmen Jacquet</span>, responsable RSE, La Poste Direction Services-Courrier-Colis de Paris </p>


