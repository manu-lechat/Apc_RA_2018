          <h2 class="titre_bgcolor"><span>Les Cafés-Climat de l’APC :</span><br><span>une nouvelle formule en 2017</span></h2>

          <p class="txt_intro">
          En 2017, pour toucher un public plus large, l’APC a proposé des sujets plus proches de l’actualité ou du quotidien des Parisiens en s’appuyant sur des partenaires qui n’appartiennent pas au champ du développement durable. Enfin, la nouvelle formule propose un format plus ludique et plus participatif.
          </p>
          <p class="txt_exergue">2 Cafés-Climat <br>et 120 participants</p>
          <h2 class="titre_majBoldColor">Café-Climat «&#8239;Manger en 2050&#8239;»</h2>

           <p class="center"><img src="img/chapitre2/manger_en_2050.jpg" alt="" style="width:60%"></p>

          <p>
          Ce premier Café-Climat nouvelle formule a été organisé le 3 Juillet à la Recyclerie (18<sup>e</sup> arrondissement), sur le thème de l’alimentation dans un futur bas-carbone. L’idée était de présenter les enjeux théoriques de l’impact carbone de l’alimentation tout en leur proposant de se projeter dans un futur bas-carbone désirable et de faire appel à leurs sens pour une dégustation d’aliments innovants.   </p>

          <p class="bold">Deux experts ont ouvert le débat : </p>
          <p>Patrick Koumarianos, Chef de projet Alimentation du Territoire, Mairie de Paris  et Jean-Luc Fessard, journaliste spécialiste du développement durable et fondateur de l’association Bon Pour le Climat</p>
          <p><span class="bold">Puis 4 startups ont été invitées à pitcher avant une dégustation des aliments proposés dans le cadre de stands en extérieur :</span>
           </p>
           <ul>
            <li>
           <span class="bold">ALGAMA</span>  met au point des produits destinés à l’alimentation humaine qui exploitent le potentiel nutritionnel des micro-algues.
          <span class="bold">JIMINI’S</span> cherche à faire rentrer les insectes dans notre alimentation en mettant en avant leurs qualités gustatives et nutritionnelles.</li>
          <li>
           <span class="bold">NextAlim</span> mise sur les protéines et huiles d’insectes pour l’alimentation animale et l’industrie, en développant le potentiel de l’entomoculture.</li>
          <li>
           <span class="bold">Le Paysan Urbain</span> est une structure d’agriculture urbaine développant des fermes urbaines agro-écologiques, économiquement viables, socialement responsables et inscrites dans leur territoire tout en étant connectées aux agriculteurs ruraux et péri-urbains.</li>
         </ul>


