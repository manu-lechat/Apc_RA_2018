      <p class="txt_intro">
        La Ville de Paris mobilise, au travers de la Charte <br><span class="bold">«&#8239;Paris Action Climat&#8239;»</span>, les représentants économiques et institutionnels parisiens pour inscrire son ambition dans la réalité économique et valoriser les actions engagées par le secteur privé et institutionnel. A ce jour, une quarantaine d’entreprises et institutionnels parisiens ont reconnu le risque que pouvait constituer le changement climatique pour leur modèle économique, mais également les opportunités offertes par la transition écologique.  Les signataires de cette Charte se sont engagés à se fixer individuellement des objectifs spécifiques contraignants pour limiter leur empreinte carbone, développer des produits et services plus vertueux et diffuser des modes de travail et des pratiques plus durables.

