          <p class="txt_intro">L’APC informe et sensibilise les citoyens sur le changement climatique à Paris et les moyens d’agir pour réduire les émissions de CO2 et les consommations d'énergie</p>

          <h2 class="titre_bgcolor"><span>Une plateforme de référence</span><br><span> sur le climat à Paris</span></h2>
          <p class="">
            En 2017, l’APC se positionne comme la plateforme de référence sur l’énergie et le climat à Paris, avec 2 objectifs :
            <ul class="liste_puces">
            <li>Faciliter la compréhension des enjeux énergétiques et climatiques,</li>
            <li>Diffuser les bonnes pratiques et s’appuyer sur les témoignages positifs pour développer l’engagement de tous les acteurs du territoire dans la transition énergétique.</li>
            </ul>
          </p>
        </div>
        <div class="col_33pc">
          <p>Véritable portail d’informations et de solutions, le site internet de l’APC édite régulièrement des actualités et des publications, fidèles à la pédagogie de l'agence : <span class="asap_bold_italic">«&#8239;Comprendre pour agir&#8239;»</span>.  Il ambitionne d’être une base solide de connaissances sur les fondamentaux du climat et de l’énergie à Paris.
          </p>
          <p class="bold">
          Pour sensibiliser les Parisiens, chaque saison, l’Agence Parisienne du Climat (APC) publie avec Météo-France un bulletin climatique pour comparer la saison qui vient de passer avec les moyennes des mesures des 30 dernières années à Paris. <br>
          </p>
          <p class="">En 2017, le climat a souvent fait parler de lui : tempêtes, inondations, incendies, sécheresses, chaleur record au niveau mondial. A Paris, les données récoltées par Météo-France témoignent également d’une année particulièrement chaude, en ligne avec les projections des climatologues.
          </p>
