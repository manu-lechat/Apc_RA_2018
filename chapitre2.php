        En 2017, les communautés ont été sollicités pour contribuer à l’élaboration du <span class="bold">nouveau Plan Climat</span>. L’APC a coorganisé dans ce cadre 6 ateliers de concertation avec les communautés Commerces, Tourisme, Immobilier Durable, Sport culture Loisirs, Santé, Enseignement supérieur.
