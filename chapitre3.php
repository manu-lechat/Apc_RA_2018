                    <h2 class="titre_bgcolor">
                      <span>Le CoachCopro ,</span><br>
                      <span>reconnu en Europe : </span><br>
                      <span>Ace-Retroffiting </span>
                    </h2>


                  <img src="img/chapitre3/logo_ACE.jpg" alt="">

                    <p class="txt_intro">
                      L’Agence Parisienne du Climat, forte de son expertise en matière d’éco-rénovation des copropriétés, est partenaire de Energy-Cities, coordinateur du projet ACE-Retroffiting  avec six villes de cinq pays différents : Paris, Liège, Maastricht, Anvers, Aberdeen, Francfort.
                    </p>

                    <p class="">
                    En devenant partenaire d’Energy-Cities, l’APC intègre un réseau européen d’excellence pour l’accélération de la transition énergétique et le partage des bonnes pratiques en Europe.
                    </p>
                    <p class="">
                    L’objectif du projet  européen vise à mettre en place une stratégie pour lever les freins réglementaires, financiers et humains de la rénovation des copropriétés et permettre concrètement d’améliorer la performance énergétique de 15 000 foyers par an.
                    </p>
                    <p class="">
                    Le CoachCopro, outil collaboratif développé par l’Agence Parisienne du Climat et conçu pour être adaptable à d’autres territoires, est un élément central de cette stratégie.
                    </p>



                  </div>
                  <div class="col_33pc">
                    <div>
                      <img src="img/chapitre3/ACE_retrofitting.jpg" alt="">
                      <span class="credits_photo_black">© APC</span>
                    </div>


                                      <p class="txt_intro">
                                        A mi- étape du projet (septembre 2016 – mars 2020), l’Agence ancre son rôle de précurseur et s’inscrit dans une dynamique de transfert d’expériences pour accélérer la rénovation énergétique dans le parc privé du nord-ouest de l’Europe.
                                      </p>
                    <p>Le projet est conduit sur une durée de 3 ans et vise la réalisation d’actions concrètes : </p>
                    <ul>
                      <li><span class="bold">une boîte à outils</span> à destination des copropriétés pour dépasser les obstacles à la rénovation, augmenter et accélérer les projets de rénovation énergétique des copropriétés,
                      </li>
                        <li><span class="bold">un cadre de formation</span> pour améliorer la montée en compétence des professionnels du bâtiment sur le secteur spécifique de la rénovation en copropriété,
                        </li>
                        <li><span class="bold">un modèle de gouvernance</span> animé par les autorités locales qui relie l’offre et la demande, ainsi qu’un outil en ligne adaptable à l’Europe du Nord-ouest pour l’information et le suivi des rénovations basé sur le CoachCopro,
                        </li>
                        <li><span class="bold">une campagne européenne</span> gérée par Energy Cities qui, 10 ans après le projet, impliquera des centaines de collectivités locales pour des milliers de rénovations.
                        </li>
                    </ul>

