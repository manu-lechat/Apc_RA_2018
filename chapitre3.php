              <span>L’Observatoire métropolitain </span><br>
              <span>de la rénovation énergétique </span><br>
              <span>en copropriété se structure</span> </h2>
          </div>

                <div class="row ">
                  <div class="col_33pc">
                    <p class="txt_intro">L’observatoire a pour vocation de témoigner de la dynamique métropolitaine de la rénovation énergétique, à travers l’analyse des tendances et les caractéristiques du marché.</p>
                    <p>Porté par l’Agence Parisienne du Climat, il est adossé au dispositif CoachCopro de la métropole parisienne. Son ambition : accélérer la massification des rénovations énergétiques des copropriétés sur ce territoire. À ce titre, l’observatoire a pour mission d’améliorer la visibilité de ce marché, d’évaluer le taux de rénovations au regard des politiques publiques, de produire des analyses sur des sujets spécifiques et de valoriser les projets existants.</p>
                    <p class="bold">L’originalité de l’observatoire repose sur l’utilisation de données de terrain collectées par les Point Rénovation Info Service de la métropole via l’outil CoachCopro.</p>
                    <p>D’autres données ont commencé à être intégrées sur le territoire de la ville de Paris grâce à un travail de communication entre bases de données effectué avec les opérateurs SOLIHA et URBANIS. Cet élargissement du champ d’observation a vocation à se poursuivre pour intégrer des données d’autres partenaires.</p>
                    <p class="txt_verbatim">La plateforme CoachCopro et les travaux de l'observatoire couvrent aujourd’hui 9 des 12 territoires que compte la métropole.</p>

